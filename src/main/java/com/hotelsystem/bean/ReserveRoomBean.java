package com.hotelsystem.bean;

import java.util.Date;
import java.util.List;

/**
 * 预订房间信息
 * 
 * 预订编号，入住时间，退房时间，抵达时间，付款金额，订单状态，，入住人信息
 * 
 * @author 籍慧中
 * 
 */
public class ReserveRoomBean {
	private int id;
	private Date checkIn;
	private Date checkOut;
	private Date arrive;
	private double money;
	private int state;
	private ReservePeopleBean reservePeopleId;
	private List<CheckInPersonBean> checkInList;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Date getCheckIn() {
		return checkIn;
	}

	public void setCheckIn(Date checkIn) {
		this.checkIn = checkIn;
	}

	public Date getCheckOut() {
		return checkOut;
	}

	public void setCheckOut(Date checkOut) {
		this.checkOut = checkOut;
	}

	public Date getArrive() {
		return arrive;
	}

	public void setArrive(Date arrive) {
		this.arrive = arrive;
	}

	public double getMoney() {
		return money;
	}

	public void setMoney(double money) {
		this.money = money;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public ReservePeopleBean getReservePeopleId() {
		return reservePeopleId;
	}

	public void setReservePeopleId(ReservePeopleBean reservePeopleId) {
		this.reservePeopleId = reservePeopleId;
	}

	public List<CheckInPersonBean> getCheckInList() {
		return checkInList;
	}

	public void setCheckInList(List<CheckInPersonBean> checkInList) {
		this.checkInList = checkInList;
	}
}
